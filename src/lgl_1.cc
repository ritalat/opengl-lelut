class LGL_1: public GLleluCamera
{
public:
    LGL_1(int argc, char *argv[]);
    virtual ~LGL_1();
    virtual Status render();

    Shader texturedShader;
    Texture texture0;
    Texture texture1;
    unsigned int VAO;
    unsigned int VBO;
};

LGL_1::LGL_1(int argc, char *argv[]):
    GLleluCamera(argc, argv)
{
    glEnable(GL_DEPTH_TEST);

    texturedShader.load("textured.vert", "textured.frag");

    texture0.load("lgl_container.jpg");
    texture1.load("lgl_awesomeface.png");

    texturedShader.use();
    texturedShader.set_int("texture0", 0);
    texturedShader.set_int("texture1", 1);

    glGenVertexArrays(1, &VAO);
    glBindVertexArray(VAO);

    unsigned int VBO;
    glGenBuffers(1, &VBO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);

    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);

    glBindVertexArray(0);
    glBindBuffer(GL_ARRAY_BUFFER, 0);
}

LGL_1::~LGL_1()
{
    glDeleteVertexArrays(1, &VAO);
    glDeleteBuffers(1, &VBO);
}

Status LGL_1::render()
{
    glm::mat4 projection = glm::perspective(glm::radians(camera.fov), (float)fbSize.width / (float)fbSize.height, 0.1f, 100.0f);

    glClearColor(0.2f, 0.3f, 0.3f, 1.0f);
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

    texture0.activate(0);
    texture1.activate(1);

    texturedShader.use();

    texturedShader.set_mat4("projection", projection);
    texturedShader.set_mat4("view", view);

    glBindVertexArray(VAO);
    int ncubes = sizeof(cubePositions) / sizeof(glm::vec3);
    for (int i = 0; i < ncubes; ++i) {
        glm::mat4 model = glm::mat4(1.0f);
        model = glm::translate(model, cubePositions[i]);
        float angle = 20.0f * i;
        model = glm::rotate(model, glm::radians(angle), glm::vec3(1.0f, 0.3f, 0.5f));

        texturedShader.set_mat4("model", model);

        glDrawArrays(GL_TRIANGLES, 0, 36);
    }

    SDL_GL_SwapWindow(window);

    return Status::Ok;
}

